public class AsteriskPattern{

 public static void main(String[] args){

  int i = 4;
  while (i >= 1){
    int j = 1;
    while (j >= 1){
     System.out.print("* * * * * * * * *  ");
     System.out.println(" ");
     j--;
    }
     System.out.print("  * * * * * * * * *");
     System.out.println(" ");
     i--;
  }


  }





}