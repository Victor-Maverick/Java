public class TimesTable{

  public static void main(String[] args){
    
    for(int count = 1; count <= 12; count++){

      

       for(int index = 1; index <= 12; index++){
         System.out.printf("%d x %d = %d\t", index, count, (index * count));
     
       }
         System.out.println();
     }


  }





}